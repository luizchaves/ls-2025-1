// undefined
console.log(undefined);

// null
console.log(null);

// boolean
console.log(true);
console.log(false);

// number (integer, float)
console.log(15);
console.log(0b1111);
console.log(0o17);
console.log(0xf);
console.log(3.14);
console.log(314e-2);
// IEEE 754
// https://0.30000000000000004.com/
console.log(0.1 + 0.2); // 0.30000000000000004
// NaN (Not a Number)
console.log(1 + undefined); // NaN
// Infinity
console.log(1 / 0); // Infinity

// string
// console.log("Hello");
console.log('Hello');
console.log(`Hello`);

// array
console.log(['Alice', 'alice@email.com', 18]);

// json
console.log({
  name: 'Alice',
  email: 'alice@email.com',
  age: 18,
});
